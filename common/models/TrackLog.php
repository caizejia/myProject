    public function addComone($data)
    {
        foreach ($data as $v) {
            $v['md5'] = md5($v['order_id'] . $v['track_date'] . $v['track_status']);
            if (!(TrackLog::find()->where(['md5' => $v['md5']])->one())) {
                Yii::$app->db->createCommand()->insert('track_log', $v)->execute();
            }
        }

        $res = Yii::$app->db->createCommand("select track_status from track_log where order_id={$data[0]['order_id']}")->queryAll();
        $val = '';
        foreach ($res as $v) {
            $val .= $v['track_status'];
        }

        if (strpos($val, 'Return to Sender LT') !== false || strpos($val, 'Returned to Warehouse') !== false) {
            Yii::$app->db->createCommand()->update('orders', ['status' => '拒签', 'track_status' => 'Exception'], "id = {$data[0]['order_id']}")->execute();
            return '拒签!';
        } elseif (strpos($val, 'Completed') !== false or strpos($val, 'Delivered') !== false) {
            Yii::$app->db->createCommand()->update('orders', ['status' => '已签收', 'track_status' => 'Delivered'], "id = {$data[0]['order_id']}")->execute();
            return '已签收!';
        } else {
            Yii::$app->db->createCommand()->update('orders', ['status' => '待签收', 'track_status' => 'Pending'], "id = {$data[0]['order_id']}")->execute();
            return '待签收!';
        }
    }

